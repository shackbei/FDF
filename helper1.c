#include "fdf.h"

int	ft_check_in(char *file)
{
	int	i;

	i = ft_strlen(file);
	i--;
	if (file[i] == 'f' && file[i - 1] == 'd'
		 && file[i - 2] == 'f' && file[i - 3] == '.')
		return (1);
	return (0);
}
